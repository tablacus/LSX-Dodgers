	CALL	SEEK
	JR	C,FINISH_DISK
	LD	A,4DH		;WRITE ID
	RST	20H
	LD	A,D		;HD US1 US0
	AND	1
	ADD	A,A
	ADD	A,A
	OR	C
	RST	20H
	LD	A,(S_PARAMS+5)	;N
	LD	C,A
	RST	20H
	LD	A,(S_PARAMS+1)	;SC
	LD	B,A
	RST	20H
	LD	A,(S_PARAMS+2)	;GPL
	RST	20H
	LD	A,0E5H		;D
	RST	20H
	LD	E,1		;R
WRITE_ID2:
	EI
	HALT
	IN	A,(0FAH)	;FDC μPD765A ステータスレジスタリード
	AND	020H		;NO SERVICE
	JR	Z,ERROR_DISK
	LD	A,D		;C
	SRL	A
	OUT	(0FBH),A	;FDC μPD765A データレジスタ

	EI
	HALT
	IN	A,(0FAH)	;FDC μPD765A ステータスレジスタリード
	AND	020H		;NO SERVICE
	JR	Z,ERROR_DISK
	LD	A,D		;H
	AND	1
	OUT	(0FBH),A	;FDC μPD765A データレジスタ

	EI
	HALT
	IN	A,(0FAH)	;FDC μPD765A ステータスレジスタリード
	AND	020H		;NO SERVICE
	JR	Z,ERROR_DISK
	LD	A,E		;R
