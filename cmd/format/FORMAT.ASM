SEEK		EQU	$7C09
FDCOMMAND	EQU	$7C0C
CHECKRESULT	EQU	$7C0F
GETDEVSTAT	EQU	$7C12
RDFDC		EQU	$7C15

S_PARAMS	EQU	$7C30
S_UNITNO	EQU	S_PARAMS	;ユニット番号 0:ドライブA 1:ドライブB
S_DPS		EQU	S_PARAMS+1	;1トラックのセクタ数
S_SECTOR_R	EQU	S_PARAMS+2	;セクタ番号
S_TRACK		EQU	S_PARAMS+3	;トラック番号
