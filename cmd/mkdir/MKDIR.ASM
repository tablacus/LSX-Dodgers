;
; MKDIR Gaku
;
; ディレクトリを作成する
;
SYSTEM	EQU	00005H
JP_HL	EQU	0000FH
FCB1	EQU	0005CH
FCB2	EQU	0006CH
DTA1	EQU	00080H

	ORG 00100H
	JP	START		;0C3HはZ80ではJPだが、i8086ではRETなので
START:				;間違えてMS-DOS環境で実行しても即終了できる
	LD	DE,FCB1
	LD	C,011H		;SFIRST FCB ファイル検索 初回 CP/M
	CALL	SYSTEM
	OR	A
	JP	Z,SHOW_EXISTS	;すでに存在する場合はエラー

	LD	DE,FCB1
	LD	A,(FCB1+1)
	CP	020H
	JP	Z,SHOW_TITLE

	LD	C,016H		;FMAKE FCB ファイル作成 CP/M
	CALL	SYSTEM
	OR	A
	JP	NZ,SHOW_NOT_CREATE	;作成に失敗する場合はエラー

	LD	A,(FCB1+29)	;ディレクトリ・モード
	OR	A
	JR	Z,ROOT

	LD	HL,(FCB1+14)	;アクセスするディレクトリのクラスタ番号+1
	LD	A,H
	OR	L
	JR	Z,ROOT
	DEC	HL
	LD	(PARENTCL),HL
ROOT:
	LD	HL,FCB1
	LD	E,(HL)
	LD	C,01BH		;ALLOC ディスク情報の獲得
	CALL	SYSTEM
	CP	0FFH
	JP	Z,SHOW_NOT_CREATE	;取得できない場合はエラー
	PUSH	AF		;A←1クラスタのセクタ数(1,2,4,8,16)
	LD	(SECPAT+1),BC	;BC←論理セクタのサイズ

	LD	HL,DIRENT3;
	LD	DE,DIRENT3+1;
	DEC	BC
	LDIR			;0クリア

	LD	HL,0
	LD	(FCB1+35),HL	;ランダム・レコード
	LD	(FCB1+33),HL	;ランダム・レコード
	INC	H
	LD	(FCB1+14),HL	;レコード・サイズ
	POP	BC		;クラスタのセクタ数(1,2,4,8,16)

	LD	DE,SUBD
LOOP1:
	PUSH	BC
	LD	C,01AH		;SETDTA ディスク転送アドレスの設定 CP/M
	CALL	SYSTEM
SECPAT:	LD	HL,512		;論理セクタのサイズ
	LD	DE,FCB1
	LD	C,026H		;WRBLK FCB ランダムブロック書き込み
	CALL	SYSTEM
	POP	BC
	OR	A
	JR	NZ,SHOW_NOT_CREATE
	LD	DE,DIRENT3
	DJNZ	LOOP1

	LD	HL,(FCB1+26)	;ファイルの先頭クラスタ
	LD	(SELFCL),HL

	LD	HL,0
	LD	(FCB1+35),HL	;ランダム・レコード
	LD	HL,SELFCL-SUBD
	LD	(FCB1+33),HL	;ランダム・レコード

	LD	E,L
	LD	D,H
	LD	C,01AH		;SETDTA ディスク転送アドレスの設定 CP/M
	CALL	SYSTEM

	LD	DE,FCB1
	LD	HL,2
	LD	C,026H		;WRBLK FCB ランダムブロック書き込み
	CALL	SYSTEM
	OR	A
	JR	NZ,SHOW_NOT_CREATE

	LD	A,010H		;ディレクトリ
	LD	(FCB1+13),A	;属性(アトリビュート)
	LD	HL,0
	LD	(FCB1+16),HL	;ファイルのサイズ

	LD	DE,FCB1
	LD	C,010H		;FCLOSE FCB ファイルクローズ CP/M
	CALL	SYSTEM

	LD	DE,OKMES
	JR	EXITMES

SHOW_TITLE:
	LD	DE,MES_TITLE
	LD	C,9		;STROUT 文字列出力 CP/M
	CALL	SYSTEM
	LD	DE,ERRMES1
EXITMES:
	LD	C,9		;STROUT 文字列出力 CP/M
	CALL	SYSTEM
	JP	0
SHOW_MESSAGE:
	LD	C,9		;STROUT 文字列出力 CP/M
	CALL	SYSTEM
	LD	DE,CRLF
	LD	C,9		;STROUT 文字列出力 CP/M
	CALL	SYSTEM
	JP	0
SHOW_NOT_CREATE:
	LD	DE,MES_NOT_CREATE
	JR	SHOW_MESSAGE
SHOW_EXISTS:
	LD	DE,MES_EXISTS
	JR	SHOW_MESSAGE

MES_TITLE:
	DB	"mkdir v1.03 Gaku",00DH,00AH
	DB	'$'
ERRMES1:
	DB	"usage: MKDIR directory",00DH,00AH
	DB	'$'
OKMES:
	DB	"OK"
CRLF:	DB	00DH,00AH
	DB	'$'
MES_EXISTS:
	DB	"File exists$"
MES_NOT_CREATE:
	DB	"Cannot create directory$"

SUBD:
	DB	".          "
	DB	010H
	DW	0,0,0,0,0
TIME1:	DW	0
DATE1:	DW	0
SELFCL:	DW	0
	DW	0,0

	DB	"..         "
	DB	010H
	DW	0,0,0,0,0
TIME2:	DW	0
DATE2:	DW	0
PARENTCL:
	DW	0
	DW	0,0
DIRENT3:
	DB	0
