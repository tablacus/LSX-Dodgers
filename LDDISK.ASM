
;	LSX-Dodgers DISK


SEEK:
	LD	A,D
	OR	E
	LD	A,001H
	JR	Z,DIV3

	LD	B,16
MAXSEC:	LD	C,000H
	XOR	A
	EX	DE,HL
DIV1:
	ADD	HL,HL
	ADC	A,A
	CP	C
	JR	C,DIV2
	SUB	C
	INC	L
DIV2:
	DJNZ	DIV1

SECPAT:	ADD	A,001H		;(Self-rewriting)
	LD	D,L	;Quotient(TRACK)
DIV3:
	LD	E,A	;Remainder
	SRL	D	;CYLINDER
	SBC	A,A
	AND	010H	;SIDE
	LD	C,A

	LD	A,(_DRIVE)
	CP	4
	CCF
	RET	C
	OR	C
	LD	(MOPAT+1),A	;Motor off data
	LD	BC,00FFCH
	OR	080H		;Mortor on
	OUT	(C),A

	CALL	SETMODE
	RET	C

	CALL	DRIVE
	CP	0FFH		;A=0FFH Unknown CYLINDER
	CALL	Z,FDCRES	;Restore
	LD	C,0F9H
	OUT	(C),A		;Currrent CYLINDER
	SUB	D
	RET	Z		;No seek

	LD	A,(_ISEEK)
	LD	C,A

	LD	A,(_MAXCYL)	;Max CYLINDER
	DEC	A
	CP	D		;Over CYLINDER
	RET	C

	CP	C		;for Built-in 2DD
	JR	C,SETM2
	LD	A,B		;B=00FH
	IN	A,(0FEH)		;2HD mode for 2DD seek
SETM2:
	LD	A,B
	IN	A,(0FDH)		;MFM mode

	LD	C,0FBH
	OUT	(C),D
	LD	(HL),D
	LD	C,018H		;Seek
FDCCMD2:
	LD	A,(_SEEKSP)
FSPAT:	AND	0FFH
	OR	C

FDCCMD:
	CALL	COMSET

FDCPAT:	LD	A,080H		;FDC COMMAND(Self-rewriting)
	AND	020H		;Write data Write track
	INC	A

SST:
	LD	C,0
SST1:
	DEC	C		; 4
	JR	NZ,SST1		;12 * 256 / 4 = About 1ms
	DEC	A
	JR	NZ,SST1

	INC	A		;A=1
	CALL	READY
SETMODE:
	LD	A,B		;B=00FH
FDMODE:	IN	A,(0FFH)		;Floppy mode(2D/2HD) (Self-rewriting)

	LD	A,B
	IN	A,(0FDH)		;MFM mode

	CALL	DELAY0		;Head select time
	LD	A,081H
READY:
	LD	(WAITA+1),A
	PUSH	HL
	LD	C,0F8H
	LD	H,C
READY2:
	IN	A,(C)
WAITA:	AND	081H		;NOT READY,BUSY
	JR	Z,READYE
	EX	(SP),HL		;wait 19clock
	EX	(SP),HL		; //
	DEC	HL
	LD	A,H
	OR	L
	JR	NZ,READY2
	SCF
READYE:
	POP	HL
	RET

FDCRES:
	LD	(HL),0
	LD	C,8
	JR	FDCCMD2

FDMOFF:
	PUSH	AF
	PUSH	BC
	LD	BC,00FFCH	;Motor off
MOPAT:	LD	A,000H		;(Self-rewriting)
	OUT	(C),A
	POP	BC
	POP	AF
	RET

SECSET:
	LD	BC,00FFAH
	OUT	(C),E
COMSET:
	LD	C,0F8H
	OUT	(C),A
DELAY0:
	LD	A,26
DELAY:
	DEC	A
	JR	NZ,DELAY
	RET

DRIVE:
	LD	HL,(_DRIVE)
	LD	H,_CYL0/256
	SET	7,L
	LD	A,(HL)
	RET

RNF:
	CALL	DRIVE
	LD	(HL),0FFH
	RET

RETRY:	DB	2


;	FLOPPY DISK DRIVER(DMA)


WTTRK:
	LD	A,0F4H
	JR	WTTRK1
FDWT:
	LD	A,0A0H
WTTRK1:
	LD	(FDCPAT+1),A
	LD	A,16
	JR	DISK

FDRD:
	LD	A,080H
	LD	(FDCPAT+1),A
	LD	A,14

DISK:
	LD	(DMAPAT+1),A

	LD	(DMAD+11),HL
DISKR:
	LD	A,2		;Retry count
	LD	(RETRY),A
DISK2:
	PUSH	DE
	CALL	SEEK
	JR	C,ERRF
	DI

DMAPAT:	LD	A,14		;���ȏ�������
	LD	HL,DMAD
	CALL	SETDMA
	OUT	(C),C		;OUT 01F87H,087H
	LD	A,(FDCPAT+1)
	PUSH	BC
	CALL	SECSET
	LD	HL,(DMAD+11)
	INC	HL
	LD	DE,0BB06H	;READ DMA

	LD	A,3
	CALL	READY
	IN	A,(C)

	POP	BC
	OUT	(C),D
	OUT	(C),E
	IN	E,(C)
	IN	D,(C)
	ADD	HL,DE
	POP	DE
	INC	DE
	EI
	CALL	FDMOFF
	OR	A
	RET	Z
	DEC	DE
	BIT	4,A
	CALL	NZ,RNF
DISKE2:
	LD	HL,RETRY
	DEC	(HL)
	JR	NZ,DISK2
	OR	A
	JR	Z,ERR
	PUSH	DE
ERRF:
	POP	DE
DELP:
	CALL	RNF
	CALL	FDMOFF
	LD	A,(_SEEKSP)
	ADD	A,A
	SBC	A,A
	JR	C,ERR
DELP1:
	PUSH	DE
	LD	HL,DEMES
	CALL	MSX
	CALL	KEYBC
DELP2:
	CALL	_INKEY
	JR	Z,DELP2

	POP	DE
	CALL	CAP
	CP	'R'		;Retry
	JP	Z,DISKR
	CP	'I'		;Ignore
	JR	Z,IGNORE
	CP	'A'		;Abort
	JR	NZ,DELP1
	JP	BOOT
IGNORE:
	LD	A,0FFH
ERR:
	CP	A
	SCF
	RET

SETDMA:
	LD	BC,01F87H
SDMA:
	INC	B
	OUTI
	DEC	A
	JR	NZ,SDMA
	RET

DMAD:	DB	0C3H,07DH,0FBH,00FH,0FFH,0FFH,02CH,010H		;X1turbo
	DB	080H,092H,08DH,000H,000H,0CFH,001H,0CFH		;DISK DMA

DMAE:

AT_R	EQU	WTTRK-AT_WTTRK
