SETTMSX:			;FCBに日付時刻をセットする
	CALL	_SYS2C		;(BDOS)時刻の獲得
				;H←時  L←分  D←秒
	SLA	L		;L=L/2
	SLA	L		;L=L/2
	ADD	HL,HL		;*2
	ADD	HL,HL		;*4
	ADD	HL,HL		;*8
	LD	A,D
	RRCA			;bit.0->7->->->0->C
	AND	01FH
	OR	L
	LD	(IY+22),A	;(FCB)ファイルを最後に変更した時刻
	LD	(IY+23),H

	CALL	_SYS2A		;(BDOS)日付の獲得
				;HL←年  D←月  E←日
	LD	BC,0-1980
	ADD	HL,BC
	LD	H,L

	LD	A,D
	ADD	A,A		;*2
	ADD	A,A		;*4
	ADD	A,A		;*8
	ADD	A,A		;*16
	LD	L,A
	ADD	HL,HL		;*32
	LD	A,L
	OR	E
	LD	(IY+20),A	;(FCB)ファイルを最後に変更した日付
	LD	(IY+21),H
	RET

