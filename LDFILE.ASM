	OR	E
	EX	DE,HL		;
	CALL	DIVSPC		;ここでフラグは変化しない
	EX	DE,HL		;
	JR	NZ,WRDF2
	LD	(IY+26),L	;(FCB)ファイルの先頭クラスタ
	LD	(IY+27),H	;元が空の場合はFCBにクラスタをセットする
