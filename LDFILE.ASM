	POP	DE
	POP	BC
	RET	C
	DJNZ	WRDF5		;ここでループカウンタチェック
	LD	DE,(_CLPS)
	CALL	DIVSPC
	LD	HL,0FFFFH	;FATエントリ終了(EOCマーク)
	JP	_SNCL

