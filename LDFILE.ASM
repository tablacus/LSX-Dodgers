	JR	C,WRDF3
	LD	A,D		;空いているかチェック
	OR	E
	CALL	MULSPC
	JR	NZ,WRDF4
	POP	HL		;HL=空いているクラスタ
	PUSH	HL
	LD	DE,(_CLPS)	;DE=元の(_CLPS)
	LD	(_CLPS),HL
