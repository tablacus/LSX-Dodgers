;------------------------
;FATのセットアップ
;out
;	B  : FATセクタ数
;	DE : FAT先頭セクタ番号
;	HL : FATバッファポインタ
;	CF : 1=ドライブ切り替えエラー
;note
;	FATサイズがFATバッファを超える場合は
;	対象クラスタ領域==(_FATDRV).bit7によって
;	FATの前半3KBと後半3KBを切り替える
;------------------------
FATS:
	LD	A,(_FATDRV)
	CALL	CHGDRVR		;ドライブ切り替え
	RET	C
FATS2:
	LD	DE,(_FATPS)	;fat sector pos
	LD	HL,(_FATBF)
	LD	B,(IX+0)	;DPB.FATLN
	LD	A,(IX+7)	;$29=512 / 0=1024
	OR	A
	LD	A,6		;3072 / 512 FATバッファに読み込めるセクタ数
	JR	NZ,FATS512
	BIT	7,(IX+00FH)
	JR	NZ,FATS512
	RRA			;A=3 (3072÷1024: 1セクタ1024バイトの場合)
FATS512:
	CP	B
	RET	NC		;DPB.FATS x sectorSize < fatBufSize
;targetCluster
	LD	C,A		;C = skipCount
	LD	A,(_FATDRV)	;bit7 = (targetCluster >= 0x800)
	ADD	A,A		;CF=(_FATDRV).bit7
	JR	NC,FATS3
;(targerCluster>=0x800)
FAT_SKP:
	INC	DE		;FATPS++
	DEC	B		;FATSIZE--
	DEC	C		;skipCnt--
	JR	NZ,FAT_SKP
	LD	C,B
FATS3:
	LD	B,C		;3 or 6
	XOR	A
	RET

