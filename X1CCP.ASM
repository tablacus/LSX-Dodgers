
;	LSX-Dodgers CCP
;	X1/turbo/Z

WBOOT1:
	DI
	LD	SP,(SYSTEM+1)

	LD	A,INTVEC/256
	LD	I,A

	LD	A,0E4H
	CALL	COMOUT
	LD	A,INTVEC-CPM_BOOT
	NOP			;必ずBDOS0+13Hが「0」になるようにする
	CALL	OT49SB

	LD	HL,CRTCD
	XOR	A
SETCRT2:
	LD	BC,01800H
	OUT	(C),A
	INC	C
	LD	D,(HL)
	CP	13
	JR	C,SETCRT3
	LD	D,0
SETCRT3:
	OUT	(C),D
	INC	HL
	INC	A
	CP	14
	JR	NZ,SETCRT2

	LD	BC,01A03H+00100H
	OUTI
	LD	BC,01FD0H+00100H
	OUTI
	LD	HL,_KEYD
	LD	A,(HL)
	LD	(HL),0
	CALL	KEYBC_IFBREAK
	LD	A,4
	CALL	MSG_A
