	LD	L,(IY+32)	;BPB_TotSec32 は AHL の24ビットで扱う
	LD	H,(IY+33)	;1クラスタの論理セクタ数(DPB_07_SECPCL)が最大128なので
	LD	A,(IY+34)	;24ビットを超えると必ずクラスタ数が16ビットを超えてしまうのでこれで十分
	AND	A		;CF=0
BPB16BIT:
	SBC	HL,DE
	SBC	A,0
	LD	B,(IY+13)	;BPB_SecPerClus
BPBTC1:				;総クラスタ数(AHL) = 総セクタ数(AHL) / セクタサイズ(B)
	RR	B		;->CY
	JR	C,BPBTC2
	SRL	A
	RR	H		;AHL=AHL/2
	RR	L
	JR	BPBTC1
BPBTC2:
	OR	A
	SCF
	RET	NZ		;念のため(クラスタ数が16ビットを超える場合)
	INC	HL
	INC	HL
	LD	(IX+8),L	;DPB_08_MAXCL
	LD	(IX+9),H

	LD	A,(IY+21)	;BPB_Media
	LD	(IX+6),A	;DPB_06_FATID

	BIT	7,(IX+00AH)	;DPB_0A_FDMODE
	JR	Z,NOT_FD
	LD	L,40		;MAXCYLの設定（要検討）ここから下はフロッピーのみ書き換える
	CP	0FDH	;2D
	JR	Z,SETCYL
	LD	L,77
	CP	0FEH	;2HD
	JR	Z,SETCYL
	LD	L,80	;その他2DD等
SETCYL:
	LD	(IX+00CH),L	;DPB_0C_MAXCYL
	LD	A,(IY+24)	;BPB_SecPerTr
	LD	(IX+00DH),A	;DPB_0D_MAXSEC	;ここまでフロッピー専用
NOT_FD:
	LD	A,(IY+16)	;BPB_NumFATs
	CP	2		;>2:NC
	LD	A,(IY+12)	;BPB_BytsPerSec
	JR	C,BPBFAT1
	OR	080H
BPBFAT1:
	LD	(IX+00FH),A	;DPB_0F_BPS

	AND	07FH		;セクタバッファが1KBしかないので1KBを超える場合はCF=1でエラーにする
	ADD	A,0-5		;0-4:NC 5以上:C
	RET

RDFATS:
	CALL	FATSETUP
	RET	C
RDFATL:
	PUSH	BC
	CALL	DRD16
	POP	BC
	RET	C
	DJNZ	RDFATL
	LD	HL,(_FATBF)
	LD	A,(HL)
	RET

DRD16:
	LD	C,0
DRD24:
	LD	B,1
	PUSH	IX
	LD	IX,(_DPB)
DRDPAT:	CALL	FDRD		;自己書き換え（ディスク読み込み）
POP_IX_RET:
	POP	IX
	RET

DWT16:
	LD	C,0
DWT24:
	LD	B,1
	PUSH	IX
	LD	IX,(_DPB)
DWTPAT:	CALL	FDWT		;自己書き換え（ディスク書き込み）
	JR	POP_IX_RET

WTFAT:
	CALL	FATSETUP
	CALL	NC,WTFATL
	RET	C
	BIT	7,(IX+00FH)	;DPB_0F_BPS
	RET	Z		;予備FATが無い
	CALL	FATS2
	PUSH	HL		;予備FAT
	LD	L,(IX+0)	;DPB_00_FATLN
	LD	H,(IX+1)
	ADD	HL,DE
	EX	DE,HL
	POP	HL
WTFATL:
	PUSH	BC
	CALL	DWT16
	POP	BC
	RET	C
	DJNZ	WTFATL
	RET
;------------------------
;FATのセットアップ
;out
;	B  : FATセクタ数
;	DE : FAT先頭セクタ番号
;	HL : FATバッファポインタ
;	CF : 1=ドライブ切り替えエラー
;note
;	FATサイズがFATバッファを超える場合は
;	対象クラスタ領域==(_FATIX)によって
;	FAT12:FATの前半3KBと後半3KBを切り替える
;	FAT16:対象クラスタ1024毎に2KBを切り替える
;------------------------
FATSETUP:
	LD	A,(_FATDRV)
	CALL	CHGDRVR		;ドライブ切り替え
	RET	C
FATS2:
	LD	DE,(_FATPS)	;fat sector pos
FATSX:	JP	FATSETUP12	;自己書き換え

FATSETUP12:
	LD	HL,(_FATBF)
	LD	A,24		;3072 / 256 FATバッファに読み込めるセクタ数
	LD	B,(IX+00FH)	;DPB_0F_BPS
L_FATS2:
	RRA			;CF=0 なので A=A/2
	RR	B		;->CF
	JR	NC,L_FATS2

	LD	B,(IX+0)	;DPB_00_FATLN
	CP	B
	RET	NC		;DPB_00_FATLN x sectorSize < fatBufSize
;targetCluster
	LD	C,A		;C = skipCount
	LD	A,(_FATIX)	;(targetCluster >= 0x800)
	OR	A
	JR	Z,FATS3
;(targerCluster>=0x800)
FAT_SKP:
	INC	DE		;FATPS++
	DEC	B		;FATSIZE--
	DEC	C		;skipCnt--
	JR	NZ,FAT_SKP
	LD	C,B
FATS3:
	LD	B,C		;3 or 6 or 12
	XOR	A
	RET

FATSETUP16:
	LD	A,(IX+00FH)	;DPB_0F_BPS 512=2 1024=4
	LD	B,16		;2048 / 256 FATバッファに読み込むセクタ数 512=4/1024=2
	LD	H,A		;DPB_0F_BPS
L_F16S2:
	SRL	B		;/2
	RRA			;->CF
	JR	NC,L_F16S2

	LD	A,H		;DPB_0F_BPS
	LD	HL,(_FATIX)	;範囲は0-63(0-03FH):65535*2/2048
	LD	H,0
	LD	C,L		;_FATIX
L_F16S3:
	ADD	HL,HL
	ADD	A,A
	BIT	3,A
	JR	Z,L_F16S3

	ADD	HL,DE
	EX	DE,HL

	LD	A,C		;_FATIX
	LD	L,(IX+0)	;DPB_00_FATLN
	DEC	L		;DPB_00_FATLN-1
	XOR	L		;Lと同じか比較してCFを0に
	JR	NZ,F16E		;FATバッファが最後の場合はFATセクタ数を調整
	LD	A,L		;DPB_00_FATLN-1
	DEC	B
	AND	B
	LD	B,A
	INC	B
F16E:
	LD	HL,(_FATBF)
	RET

CHGDRV:
	PUSH	HL
	LD	HL,_DSK
	CP	(HL)
	JR	Z,CHGDRVE
CHGDRV1:
	PUSH	IX
	CALL	CHGDRV0
	LD	A,(_DSK)
	POP	IX
CHGDRVE:
	POP	HL
	RET

CHGDRV0:
	LD	L,A
	CALL	_GETDPB
	RET	C
	LD	(_DPB),IX
	LD	A,L
	LD	(_DSK),A
CHGDRV2:
	DI
	PUSH	BC
	PUSH	DE
	LD	(SPPAT2+1),SP

	LD	SP,IX

	POP	HL		;DPB_00_FATLN
	POP	HL		;DPB_02_DRD
	LD	(DRDPAT+1),HL

	POP	HL
	LD	(DWTPAT+1),HL	;DPB_04_DWT

	POP	HL		;L=DPB_06_FATID H=DPB_07_SECPCL
	LD	A,H
	LD	(SPCPAT+1),A	;1クラスタの論理セクタ数
	DEC	A
	LD	(NCPAT+1),A

	POP	HL		;DPB_08_MAXCL
	LD	A,L
	LD	(CLPAT2+1),A
	LD	A,H
	LD	(CLPAT1+1),A
	DEC	HL
	LD	(MAXCLP+1),HL

	POP	HL		;L=DPB_0A_FDMODE H=DPB_0B_DIRSCNT
	LD	A,L
	OR	0FEH
	LD	(FDMODE+1),A
	RLCA
	AND	3
	LD	(FSPAT+1),A
	LD	C,H

	POP	HL		;L=DPB_0C_MAXCYL H=DPB_0D_MAXSEC
	LD	A,L
	LD	(_MAXCYL),A
	LD	A,H
	LD	(MAXSEC+1),A

	POP	HL		;L=DPB_0E_FATPS H=DPB_0F_BPS
	LD	A,H		;DPB_0F_BPS
	AND	7
	LD	(SECSZ+2),A	;1セクタのサイズの上位1バイト 0x400 - 0x200 - 0x100
				;1セクタ512 2D/2DD/2HC/2HQ(2HD1.44MB)等
				;1セクタ1024 2HD/EMM/BRAM
				;1セクタ256 GRAM等
				;1024	512	256
				;4	2	1
	ADD	A,A		;8	4	2
	ADD	A,A		;010H	8	4
	ADD	A,A		;020H	010H	8
	LD	(SDECPAT+1),A	;1セクタ辺りのディレクトリエントリ数
	DEC	A		;01FH	00FH	7
	LD	(SDECM1+1),A	;1セクタ辺りのディレクトリエントリ数-1

	LD	H,0
	LD	(_FATPS),HL

	POP	HL		;L=DPB_10_DIRPS
	LD	(_DIRPS),HL

	POP	HL		;L=DPB_12_DEVICE H=DPB_13_UNITNO
	LD	A,H
	LD	(_DRIVE),A

	POP	HL		;DPB_14_ADDCL16
	LD	(CLSPAT+1),HL

SPPAT2:	LD	SP,0		;元のSPを復元
	EI
	LD	HL,(_DIRPS)
	LD	B,0
	ADD	HL,BC
	LD	(MD_PAT+1),HL

	LD	HL,(MAXCLP+1);
	LD	DE,4086
	OR	A
	SBC	HL,DE
	JR	NC,SETFAT16
	LD	HL,GNCL		;FAT12
	LD	DE,SNCL
	LD	BC,FATSETUP12
	RES	5,(IX+00FH)	;DPB_0F_BPS(FAT12)
	JR	EXTRA1
SETFAT16:
	LD	HL,GNCL16	;FAT16
	LD	DE,SNCL16
	LD	BC,FATSETUP16
	SET	5,(IX+00FH)	;DPB_0F_BPS(FAT16)
EXTRA1:
	LD	(GNCPAT+1),HL
	LD	(SNCPAT+1),DE
	LD	(FATSX+1),BC
	POP	DE
	POP	BC
	XOR	A
	RET

GETDPBD:
	EX	(SP),IX
	PUSH	IX
	LD	A,(_DRV)
	JR	GETDPB1

CHGDRVR:
	CALL	_CHGDRV
	RET	C
	LD	A,(_DSK)
GETDPB1:
	JP	_GETDPB

GETDPB:
	CP	8
	CCF
	RET	C
	RRCA
	RRCA
	RRCA
	DB	0DDH		;Z80未定義命令
	LD	L,A		;LD	IXL,A
	DB	0DDH		;Z80未定義命令
	LD	H,DEVICE/256	;LD	IXH,DEVICE/256
	LD	A,(IX+0)	;DPB_00_FATLN
	OR	A		;CF=0の為
	BIT	6,(IX+00FH)	;DPB_0F_BPS
	RET	NZ		;FAT16
	BIT	5,(IX+012H)	;DPB_12_DEVICE
	RET	NZ		;BPB
	CP	001H		;A=0 THEN CF=1
	RET

FFLUSH:
	PUSH	AF
	LD	A,0FFH
	LD	(SFILE),A
	CALL	DWTX
	LD	A,0FFH
	LD	(_DBDRV),A
	CALL	WTFATX
	XOR	A
	LD	(_FATIX),A
	CPL
	LD	(_FATDRV),A
	POP	AF
	RET


