	PUSH	DE
	CALL	SEEK
	JR	C,ERRF
	DI

DMAPAT:	LD	A,14		;自己書き換え
	LD	HL,DMAD
	CALL	SETDMA
	OUT	(C),C		;OUT 01F87H,087H WR6 イネーブル
	LD	A,(FDCPAT+1)
	PUSH	BC
	CALL	SECSET
	LD	HL,(DMAHL)
	INC	HL
	LD	DE,0BB06H	;READ DMA

	LD	A,3
	CALL	READY
	IN	A,(C)

	POP	BC
	OUT	(C),D		;読み出しマスク設定
	OUT	(C),E		;バイトカウンタ(LH)
	IN	E,(C)
	IN	D,(C)
	ADD	HL,DE
	POP	DE
	INC	DE
	EI
	CALL	FDMOFF
	OR	A
	JR	Z,DISKOK
	DEC	DE
	BIT	4,A
	CALL	NZ,RNF
DISKE2:
	LD	HL,RETRY
	DEC	(HL)
	JR	NZ,DISK2
	OR	A
	JR	Z,ERR
	PUSH	DE
ERRF:
	POP	DE
DELP:
	CALL	RNF
	CALL	FDMOFF
	LD	A,(_SEEKSP)
	ADD	A,A
	SBC	A,A
	JR	C,ERR
DELP1:
	PUSH	DE
	LD	HL,DEMES
	CALL	MSX
	CALL	KEYBC
DELP2:
	CALL	_INKEY
	JR	Z,DELP2

	POP	DE
	CALL	CAP
	CP	'R'		;Retry
	JP	Z,DISKR
	CP	'I'		;Ignore
	JR	Z,IGNORE
	CP	'A'		;Abort
	JR	NZ,DELP1
	JP	BOOT
IGNORE:
	LD	A,0FFH
ERR:
	CP	A
	SCF
	RET

